public class CalculadoraNorberta {
    public static void main(String args[]){
        // Las variables de valo que calcularemos

        int valor1 = 20;
        int valor2 = 30;

        //  Suma
        int suma = valor1 + valor2;
        System.out.println("El valor de mi suma es " +suma);

        //  Resta
        int resta = valor1 - valor2;
        System.out.println("El valor de mi resta es " +resta);

        //  Multiplicación
        int multiplicación = valor1 * valor2;
        System.out.println("El valor de mi multiplicación es " +multiplicación);

        //  División
        int división = valor1 / valor2;
        System.out.println("El valor de mi división es " +división);
    }
}